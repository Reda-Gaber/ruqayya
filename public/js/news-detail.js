document.addEventListener('DOMContentLoaded', function() {
    const urlParams = new URLSearchParams(window.location.search);
    const newsId = urlParams.get('id');

    if (newsId && newsData[newsId]) {
        displayNewsDetail(newsData[newsId]);
        displayRelatedNews(parseInt(newsId));
    } else {
        document.getElementById('newsTitle').textContent = 'الخبر غير موجود';
        document.getElementById('newsContent').innerHTML = '<p>عذراً، الخبر المطلوب غير متوفر.</p>';
    }
});

function displayNewsDetail(news) {
    document.getElementById('newsTitle').textContent = news.title;
    document.getElementById('newsDate').textContent = news.date;

    const imageContainer = document.getElementById('newsImage');
    imageContainer.innerHTML = `<img src="${news.image}" alt="${news.title}" style="width: 100%; height: 100%; object-fit: cover;">`;

    document.getElementById('newsContent').innerHTML = news.content;
}

function displayRelatedNews(currentId) {
    const relatedContainer = document.getElementById('relatedNews');
    const filtered = relatedNews.filter(news => news.id !== currentId).slice(0, 3);

    relatedContainer.innerHTML = filtered.map(news => `
        <article class="news-card">
            <div class="news-image">
                <img src="${news.image}" alt="${news.title}" loading="lazy">
            </div>
            <div class="news-content">
                <div class="news-meta">
                    <span class="news-date">${news.date}</span>
                </div>
                <h3>${news.title}</h3>
                <p>${news.excerpt}</p>
                <a href="/pages/news-detail.html?id=${news.id}" class="news-link">اقرأ المزيد ←</a>
            </div>
        </article>
    `).join('');
}